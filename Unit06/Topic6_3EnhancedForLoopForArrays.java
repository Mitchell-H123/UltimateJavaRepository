package Unit06;

public class Topic6_3EnhancedForLoopForArrays {
    public static void main(String[] args) {
        int [] values = {12,53,762,321,-31,-5,41};
        for(int num:values){
            System.out.println(num);
        }
        System.out.println("finished");

        String [] a={"abc","def"};
        for(String i:a){
            System.out.println(i);
        }
        System.out.println("finished");
    }
}
